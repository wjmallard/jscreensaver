  let S = 1;                 // devicePixelRatio
  let W = 0, H = 0;          // canvas size, device px
  let offsetX = 0, offsetY = 0;  // screen centre, device px

  // The rotated coordinate frame: four orthonormal 4-vectors a, b, c, d, each
  // (x, y, z, w), stored row-major in R[row*4 + axis]. row 0=a 1=b 2=c 3=d;
  // axis 0=x 1=y 2=z 3=w. A vertex (a,b,c,d) maps to screen coords via
  // sum_axis = a*R[a-row,axis] + b*... ; only x,y,z are needed (w is dropped).
  const R = new Float64Array(16);

  // Per-vertex projected screen position + depth, recomputed each step.
  const SX = new Float64Array(POINT_COUNT);
  const SY = new Float64Array(POINT_COUNT);
  const DEP = new Int32Array(POINT_COUNT);

  // 64 reusable edge buckets [col][dep]; rebuilt as Path2D objects each frame.
  const paths = [];
  for (let c = 0; c < 8; c++) {
    paths.push([null, null, null, null, null, null, null, null]);
  }

  // Reset the coordinate frame to the identity orientation (frame 1 then shows
  // the polytope straight-on, with a/b as screen x/y and c as depth).
  function resetBasis() {
    R.fill(0);
    R[0] = 1;    // a = (1,0,0,0)
    R[5] = 1;    // b = (0,1,0,0)
    R[10] = 1;   // c = (0,0,1,0)
    R[15] = 1;   // d = (0,0,0,1)
  }

  // Rotate the (d0, d1) plane of all four basis vectors by `speed` units, exactly
  // as hyperball.c's rotates()/rotate() macros do (skip when the angle is zero).
  function rotPlane(d0, d1, speed) {
    const ang = speed * ANGLE_SCALE;
    if (ang === 0) return;
    const ca = Math.cos(ang);
    const sa = Math.sin(ang);
    for (let row = 0; row < 4; row++) {
      const o = row * 4;
      const u = R[o + d0];
      const v = R[o + d1];
      R[o + d0] = u * ca + v * sa;
      R[o + d1] = v * ca - u * sa;
    }
  }

  // Advance the frame by one step's worth of rotation in all six 4D planes, in
  // the same order as the C: xy, xz, yz, xw, yw, zw (axes x=0 y=1 z=2 w=3).
  function rotateBasis() {
    rotPlane(0, 1, config.xy);
    rotPlane(0, 2, config.xz);
    rotPlane(1, 2, config.yz);
    rotPlane(0, 3, config.xw);
    rotPlane(1, 3, config.yw);
    rotPlane(2, 3, config.zw);
  }

  function clear() {
    ctx.fillStyle = '#000';
    ctx.fillRect(0, 0, W, H);
  }

  // unit_scale/offsets from set_sizes(); offsets here, unit_scale recomputed in
  // step() so the Zoom (observer-z) slider applies live.
  function setSizes() {
    S = window.devicePixelRatio || 1;
    W = canvas.width;
    H = canvas.height;
    offsetX = 0.5 * (W - 1);
    offsetY = 0.5 * (H - 1);
  }

  // One simulation+draw step (hyperball_draw): clear, project every vertex with
  // the current frame, bucket edges by colour+depth into Path2D, stroke once per
  // bucket, then rotate the frame for the next step (draw-then-rotate, as the C).
  function step() {
    clear();

    const observerZ = Math.max(1.125, config.observerZ);
    const varr = Math.sqrt(observerZ * observerZ - 1.0);
    const minDim = W < H ? W : H;
    const unitScale = 0.4 * minDim * varr;

    // Pull the x/y/z components of the four basis vectors (w is dropped).
    const ax = R[0], ay = R[1], az = R[2];
    const bx = R[4], by = R[5], bz = R[6];
    const cx = R[8], cy = R[9], cz = R[10];
    const dx = R[12], dy = R[13], dz = R[14];

    for (let i = 0; i < POINT_COUNT; i++) {
      const o = i * 4;
      const pa = POINTS[o];
      const pb = POINTS[o + 1];
      const pc = POINTS[o + 2];
      const pd = POINTS[o + 3];
      const sumX = pa * ax + pb * bx + pc * cx + pd * dx;
      const sumY = pa * ay + pb * by + pc * cy + pd * dy;
      const sumZ = pa * az + pb * bz + pc * cz + pd * dz;
      // 1/z perspective. observerZ >= 1.125 and |sumZ| < 1, so the denominator
      // is always positive; clamp anyway as a divide-by-zero guard.
      let denom = observerZ - sumZ;
      if (denom < 1e-3) denom = 1e-3;
      const mul = unitScale / denom;
      SX[i] = sumX * mul + offsetX;
      SY[i] = sumY * mul + offsetY;
      DEP[i] = Math.floor(sumZ * -128.0) + 128;
    }

    // Reset the 64 buckets, then route each edge into [colour][depth].
    for (let c = 0; c < 8; c++) {
      const rowc = paths[c];
      for (let d = 0; d < 8; d++) rowc[d] = null;
    }
    for (let i = 0; i < LINE_COUNT; i++) {
      const o = i * 3;
      const ip = LINES[o];
      const iq = LINES[o + 1];
      const col = LINES[o + 2];
      let dep = (DEP[ip] + DEP[iq]) >> 6;   // sum of endpoint depths / 64
      if (dep < 0) dep = 0;
      else if (dep > 7) dep = 7;
      let p = paths[col][dep];
      if (!p) {
        p = new Path2D();
        paths[col][dep] = p;
      }
      p.moveTo(SX[ip], SY[ip]);
      p.lineTo(SX[iq], SY[iq]);
    }

    ctx.lineWidth = Math.max(1, 1.5 * S);   // 1.5x to offset canvas AA dimming on thin lines (cf. hypercube)
    for (let c = 0; c < 8; c++) {
      const rowc = paths[c];
      for (let d = 0; d < 8; d++) {
        const p = rowc[d];
        if (!p) continue;
        ctx.strokeStyle = COLORS[c][d];
        ctx.stroke(p);
      }
    }

    rotateBasis();
  }

  function resize() {
    const dpr = window.devicePixelRatio || 1;
    canvas.width = Math.round(window.innerWidth * dpr);
    canvas.height = Math.round(window.innerHeight * dpr);
    canvas.style.width = window.innerWidth + 'px';
    canvas.style.height = window.innerHeight + 'px';
    setSizes();
    clear();
  }

  // rAF lag-accumulator loop paced by config.delay + a measured framework
  // OVERHEAD. The xscreensaver *delay (20000 µs) is a sleep FLOOR; the real
  // per-frame cost is delay + compute, so effective fps is lower than
  // 1e6/delay. The live binary's -fps overlay reads FPS 33.0 at Load 34%
  // (delay-bound), so OVERHEAD = round(1e6/33.0) - 20000 = 10303 µs. (Without
  // it the port stepped at ~50 fps — ~1.5x too fast.) Same catch-up cap as
  // scooter so a backgrounded tab doesn't fire a burst of steps on refocus;
  // each step() does a full clear+repaint, so we never draw more than we step.
  const OVERHEAD = 10303;   // µs; calibrated against the live -fps overlay
  const MAX_CATCHUP_STEPS = 4;
  let lastTime = 0;
  let lag = 0;
  let rafId = 0;

  function frame(now) {
    if (lastTime === 0) lastTime = now;
    lag += now - lastTime;
    lastTime = now;

    const delayMs = (config.delay + OVERHEAD) / 1000;
    lag = Math.min(lag, delayMs * MAX_CATCHUP_STEPS);

    let steps = 0;
    while (lag >= delayMs && steps < MAX_CATCHUP_STEPS) {
      step();
      lag -= delayMs;
      steps++;
    }

    rafId = requestAnimationFrame(frame);
  }

  // Re-seed with the current config: reset the orientation to identity and clear.
  function reinit() {
    setSizes();
    resetBasis();
    clear();
  }

  window.addEventListener('resize', resize);
  resetBasis();
  resize();
  rafId = requestAnimationFrame(frame);

  return {
    stop() {
      cancelAnimationFrame(rafId);
      window.removeEventListener('resize', resize);
    },
    pause() { cancelAnimationFrame(rafId); rafId = 0; },
    resume() { if (!rafId) { lastTime = 0; rafId = requestAnimationFrame(frame); } },
    reinit,
    config,
    params,
  };
