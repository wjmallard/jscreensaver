// hyperball.js — hyperball packaged as a mountable module.
// start(canvas) returns { stop, pause, resume, reinit, config, params }.
//
// Port of xscreensaver's hyperball.c (Joe Keane, 2000; derived from TI Explorer
// Lisp code by Joe Keane, Fritz Mueller, and Jamie Zawinski).
// https://www.jwz.org/xscreensaver/
//
// A wireframe 2D projection of a rotating 4D "hyperball" — technically the
// 120-cell regular polytope, the 4D analog of the dodecahedron (600 vertices,
// 1200 edges). Each frame: the four orthonormal basis vectors of the polytope's
// coordinate frame are rotated by small fixed angles in up to six of the 4D
// rotation planes (xy, xz, yz, xw, yw, zw); every vertex is transformed by that
// frame, the w-axis is dropped (orthographic 4D -> 3D), and the result is
// projected to the screen with a 1/z perspective (3D -> 2D). Edges are coloured
// by a fixed per-edge hue (0..7) shaded by the summed depth of their endpoints.
//
// Rendering: SPARSE vector — 1200 line segments redrawn from scratch each frame
// onto a cleared black canvas. The C erases incrementally (it redraws each moved
// edge's OLD endpoints in the background colour, then the new edge in its colour
// — see hyperball_draw); canvas has no cheap stateful erase, so we full-frame
// repaint instead. To avoid issuing 1200 individual strokes we bucket every edge
// into one of 64 Path2D objects keyed by [colour][depth] and stroke each bucket
// once (<= 64 strokes/frame). See [[scooter]] for the sibling projection idioms
// and [[squiral]] for the loop/sizing skeleton.

